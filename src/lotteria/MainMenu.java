package lotteria;

public class MainMenu {
	int id;
	String bread;
	String meat;
	String va;
	String source;
	String name;
	int price;
	String option;
	
	public MainMenu(int id, String bread, String meat, String va, String source, String name, 
			int price, String option) {
		this.id = id;
		this.bread = bread;
		this.meat = meat;
		this.va = va;
		this.source = source;
		this.name = name;
		this.price = price;
		this.option = option;
	}
	
	public MainMenu() {}
	
	
}
